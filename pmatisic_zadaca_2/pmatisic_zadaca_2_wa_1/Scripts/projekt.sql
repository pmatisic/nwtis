    id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1, INCREMENT BY 1) NOT NULL PRIMARY KEY,
    vrsta VARCHAR(255),
    vrijeme_pristupa TIMESTAMP,
    putanja VARCHAR(1024),
    korisnik INTEGER,
    FOREIGN KEY (korisnik) REFERENCES KORISNICI(id)
);

